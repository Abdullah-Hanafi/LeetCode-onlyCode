class Solution {
    public int maxAreaOfIsland(int[][] grid) {
        int m = grid.length;
        int n = grid[0].length;
        int ans = 0;
        boolean[][] isVisited = new boolean[m][n];
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                ans = Math.max(ans, dfs(i, j, isVisited, grid));
            }
        }
        return ans;
    }


    public int dfs(int i, int j, boolean[][] isVisited, int[][] grid) {
        //ä¸‹æ ‡è¶Šç•Œ
        if (i < 0 || i >= grid.length || j < 0 || j >= grid[0].length) {
            return 0;
        }

        //å·²ç»è®¿é—®è¿‡
        if (isVisited[i][j]) {
            return 0;
        }

        //æ˜¯æµ·æ´‹ï¼Œä¸æ˜¯å²›å±¿
        if (grid[i][j] == 0) {
            return 0;
        }
        //æ²¡æœ‰è®¿é—®è¿‡ï¼Œå¯ä»¥è¿›è¡Œæ“ä½œäº†
        isVisited[i][j] = true;
        int ans = 1;
        ans += dfs(i - 1, j, isVisited, grid);
        ans += dfs(i + 1, j, isVisited, grid);
        ans += dfs(i, j - 1, isVisited, grid);
        ans += dfs(i, j + 1, isVisited, grid);
        return ans;
    }
}

